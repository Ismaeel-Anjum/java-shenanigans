public class learningswitchstatements {
    public static void main(String[] args) {

        int e = 0;

        switch(e){
            case 1:
                System.out.println("You have 1/10 Collectables.");
                break;
            case 2:
                System.out.println("You have 2/10 Collectables.");
                break;
            case 3:
                System.out.println("You have 3/10 Collectables.");
                break;
            case 4:
                System.out.println("You have 4/10 Collectables.");
                break;
            case 5:
                System.out.println("You have 5/10 Collectables.");
                break;
            case 6:
                System.out.println("You have 6/10 Collectables.");
                break;
            case 7:
                System.out.println("You have 7/10 Collectables.");
                break;
            case 8:
                System.out.println("You have 8/10 Collectables.");
                break;
            case 9:
                System.out.println("You have 9/10 Collectables.");
                break;
            case 10:
                System.out.println("You have ALL the Collectables!");

            default:
                System.out.println("You have no Collectables.");
        }
    }
}

